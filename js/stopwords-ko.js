// 토큰 정규화: 끝의 조사/어미를 1회 제거 시도 (잘리면 다시 시도)
export function stripSuffix(tok) {
  let t = tok;
  let changed = true;
  let pass = 0;
  while (changed && pass < 3 && t.length > 2) {
    changed = false;
    for (const suf of SUFFIXES) {
      if (t.length - suf.length >= 2 && t.endsWith(suf)) {
        t = t.slice(0, t.length - suf.length);
        changed = true;
        break;
      }
    }
    pass++;
  }
  return t;
}
